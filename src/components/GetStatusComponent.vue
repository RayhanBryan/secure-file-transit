<template>
  <v-container fluid class="pa-6">
    <!-- Header -->
    <v-row class="mb-4">
      <v-col cols="12">
        <h2 class="text-h4 font-weight-bold text-primary mb-2">
          <v-icon left size="large" color="primary">mdi-chart-line</v-icon>
          Status Dashboard
        </h2>
        <p class="text-subtitle-1 text-grey-darken-3">
          Monitor system status and activity in real time
        </p>
      </v-col>
    </v-row>

    <!-- Action Buttons -->
    <v-row class="mt-4">
      <v-col cols="6">
        <v-btn
          color="blue-darken-2"
          variant="outlined"
          size="large"
          @click="refreshStatus"
          :loading="refreshing"
          width="100%"
        >
          <v-icon left>mdi-refresh</v-icon>
          Refresh Status
        </v-btn>
      </v-col>
    </v-row>

    <!-- Detailed Status -->
    <v-row>
      <!-- File Processing Status -->
      <v-col cols="12">
        <v-card elevation="2" rounded="lg">
          <v-card-title class="pb-2">
            <v-icon class="text-primary mr-2">mdi-file-clock</v-icon>
            File Processing Status
          </v-card-title>
          <v-card-text>
            <div
              v-if="loading && fileStatuses.length === 0"
              class="text-center py-4"
            >
              <v-progress-circular
                indeterminate
                color="primary"
              ></v-progress-circular>
              <div class="text-caption mt-2">Loading file statuses...</div>
            </div>
            <div
              v-else-if="!loading && fileStatuses.length === 0"
              class="text-center py-4"
            >
              <v-icon size="48" color="grey-lighten-1"
                >mdi-file-question</v-icon
              >
              <div class="text-subtitle-2 text-grey-darken-1 mt-2">
                No files in process
              </div>
            </div>
            <v-data-table-server
              v-else
              :headers="headers"
              :items="fileStatuses"
              :loading="loading"
              :items-length="totalItems"
              :items-per-page="itemsPerPage"
              :items-per-page-options="[5, 10, 25, 50]"
              item-key="statusId"
              class="elevation-1"
              density="compact"
              @update:options="loadItems"
            >
              <template v-slot:item.status="{ item }">
                <div class="d-flex align-center">
                  <v-icon
                    :color="getFileStatusColor(item.status)"
                    class="mr-2"
                    size="20"
                  >
                    {{ getFileStatusIcon(item.status) }}
                  </v-icon>
                  <v-chip
                    :color="getFileStatusColor(item.status)"
                    size="small"
                    variant="flat"
                    class="text-white"
                  >
                    {{ item.status }}
                  </v-chip>
                </div>
              </template>

              <template v-slot:item.username="{ item }">
                <div class="font-weight-medium text-grey-darken-3">
                  {{ item.username }}
                </div>
              </template>

              <template v-slot:item.queue="{ item }">
                <div class="text-grey-darken-2">
                  #{{ item.order }} of {{ item.totalOrder }}
                </div>
                <v-progress-linear
                  :model-value="
                    ((item.totalOrder - item.order + 1) / item.totalOrder) * 100
                  "
                  :color="getQueuePositionColor(item.order, item.totalOrder)"
                  height="4"
                  class="mt-1"
                  rounded
                ></v-progress-linear>
                <div class="text-caption text-grey-darken-1 mt-1">
                  {{ getQueueText(item.order, item.totalOrder) }}
                </div>
              </template>

              <template v-slot:item.lastUpdated="{ item }">
                <v-tooltip>
                  <template v-slot:activator="{ props }">
                    <div
                      class="text-caption text-grey-darken-2"
                      v-bind="props"
                      style="cursor: help; text-decoration: underline dotted"
                    >
                      {{ formatTime(item.lastUpdated) }}
                    </div>
                  </template>
                  <span>{{ formatExactTime(item.lastUpdated) }}</span>
                </v-tooltip>
              </template>
            </v-data-table-server>
          </v-card-text>
        </v-card>
      </v-col>
    </v-row>

    <!-- Success/Error Alerts -->
    <AlertComponent
      v-if="alertMessage"
      :type="alertType"
      :message="alertMessage"
      class="mt-4"
    />
  </v-container>
</template>

<script>
import FileApiService from "../services/fileApi.js";
import AlertComponent from "@/components/shared/AlertComponent.vue";

export default {
  name: "GetStatusComponent",
  components: {
    AlertComponent,
  },
  data() {
    return {
      refreshing: false,
      alertMessage: "",
      alertType: "success",
      fileStatuses: [],
      loading: false,
      totalItems: 0,
      itemsPerPage: 10,
      headers: [
        {
          title: "Status",
          key: "status",
          sortable: true,
          width: "150px",
        },
        {
          title: "Username",
          key: "username",
          sortable: true,
          width: "150px",
        },
        {
          title: "Queue",
          key: "queue",
          sortable: false,
          width: "200px",
        },
        {
          title: "Last Updated",
          key: "lastUpdated",
          sortable: true,
          width: "150px",
        },
      ],
    };
  },
  mounted() {
    this.loadItems({ page: 1, itemsPerPage: this.itemsPerPage });
  },
  methods: {
    async loadItems({ page, itemsPerPage }) {
      this.loading = true;
      try {
        const offset = (page - 1) * itemsPerPage;

        const response = await FileApiService.getFileStatus(
          itemsPerPage,
          offset
        );

        if (response?.data && Array.isArray(response.data)) {
          this.fileStatuses = response.data;
          this.totalItems = response.totalData || response.total || 0;
        } else if (Array.isArray(response)) {
          this.fileStatuses = response;
          this.totalItems = response.length;
        } else {
          this.fileStatuses = [];
          this.totalItems = 0;
        }

        // Sort by lastUpdated if available
        if (this.fileStatuses.length > 0 && this.fileStatuses[0]?.lastUpdated) {
          this.fileStatuses.sort(
            (a, b) => new Date(b.lastUpdated) - new Date(a.lastUpdated)
          );
        }
      } catch (error) {
        this.showAlert("Failed to load file status", "error");
        this.fileStatuses = [];
        this.totalItems = 0;
      } finally {
        this.loading = false;
      }
    },

    async refreshStatus() {
      this.refreshing = true;
      try {
        // Trigger a refresh by calling loadItems with current pagination
        await this.loadItems({
          page: 1,
          itemsPerPage: this.itemsPerPage,
        });
        await new Promise((resolve) => setTimeout(resolve, 1000));
      } catch (error) {
        this.showAlert("Failed to refresh status", "error");
      } finally {
        this.refreshing = false;
      }
    },

    formatTime(dateString) {
      try {
        const date = new Date(dateString);
        const now = new Date();
        const diffMs = now - date;
        const diffMins = Math.floor(diffMs / 60000);

        if (diffMins < 1) return "Just now";
        if (diffMins < 60) return `${diffMins} minutes ago`;

        const diffHours = Math.floor(diffMins / 60);
        if (diffHours < 24) return `${diffHours} hours ago`;

        const diffDays = Math.floor(diffHours / 24);
        return `${diffDays} days ago`;
      } catch (error) {
        return "Unknown time";
      }
    },

    formatExactTime(dateString) {
      try {
        const date = new Date(dateString);
        const options = {
          year: "numeric",
          month: "long",
          day: "numeric",
          hour: "2-digit",
          minute: "2-digit",
          second: "2-digit",
          timeZoneName: "short",
        };
        return date.toLocaleString("en-US", options);
      } catch (error) {
        return "Time cannot be formatted";
      }
    },

    getFileStatusColor(status) {
      const colors = {
        uploading: "blue-darken-2",
        waiting: "orange-darken-2",
        processing: "blue-darken-2",
        completed: "green-darken-2",
        error: "red-darken-2",
        failed: "red-darken-2",
      };
      return colors[status] || "grey-darken-2";
    },

    getFileStatusIcon(status) {
      const icons = {
        uploading: "mdi-upload",
        waiting: "mdi-clock-outline",
        processing: "mdi-cog",
        completed: "mdi-check-circle",
        error: "mdi-alert-circle",
        failed: "mdi-close-circle",
      };
      return icons[status] || "mdi-help-circle";
    },

    getQueuePositionColor(order, totalOrder) {
      const percentage = (order / totalOrder) * 100;

      if (percentage <= 25) {
        return "green-darken-2";
      } else if (percentage <= 50) {
        return "blue-darken-2";
      } else if (percentage <= 75) {
        return "orange-darken-2";
      } else {
        return "red-darken-2";
      }
    },

    getQueueText(order, totalOrder) {
      const itemsAhead = order - 1;
      return `${itemsAhead} items ahead`;
    },

    showAlert(message, type = "success") {
      this.alertMessage = message;
      this.alertType = type;
      setTimeout(() => {
        this.alertMessage = "";
      }, 5000);
    },
  },
};
</script>

